package kr.co.toto.base.persistence.domain;

public class GameMt {

	private String gmCd;
	private String gmTye;
	private String gmName;
	private String gmEndDate;	
	private String gmTurn;
	private String mdDate;
	private String rgDate;
	private String gmSeq;
	private String gmEnd;
	
	public String getGmCd() {
		return gmCd;
	}
	public void setGmCd(String gmCd) {
		this.gmCd = gmCd;
	}
	public String getGmTye() {
		return gmTye;
	}
	public void setGmTye(String gmTye) {
		this.gmTye = gmTye;
	}
	public String getGmName() {
		return gmName;
	}
	public void setGmName(String gmName) {
		this.gmName = gmName;
	}
	public String getGmEndDate() {
		return gmEndDate;
	}
	public void setGmEndDate(String gmEndDate) {
		this.gmEndDate = gmEndDate;
	}
	public String getGmTurn() {
		return gmTurn;
	}
	public void setGmTurn(String gmTurn) {
		this.gmTurn = gmTurn;
	}
	public String getMdDate() {
		return mdDate;
	}
	public void setMdDate(String mdDate) {
		this.mdDate = mdDate;
	}
	public String getRgDate() {
		return rgDate;
	}
	public void setRgDate(String rgDate) {
		this.rgDate = rgDate;
	}
	public String getGmSeq() {
		return gmSeq;
	}
	public void setGmSeq(String gmSeq) {
		this.gmSeq = gmSeq;
	}
	public String getGmEnd() {
		return gmEnd;
	}
	public void setGmEnd(String gmEnd) {
		this.gmEnd = gmEnd;
	}	
}
